function dy = extension(t,y)
    dy=zeros(85,1);
    dy(1)=vpa(0);
    dy(2)=vpa(10*y(84));
    dy(3)=vpa(10*y(4)-0.1*y(1)*y(3));
    dy(4)=vpa(0.1*y(1)*y(3)-10*y(4)-60*y(4)*y(85)/4000);
    dy(5)=vpa(60*y(4)*y(85)/4000-60*y(5)*y(85)/4000);
    for i=2:79
        dy(i+4)=vpa(60*y(i+3)*y(85)/4000-60*y(i+4)*y(85)/4000);
    end
    dy(84)=vpa(60*y(83)*y(85)/4000-10*y(84));
    sum=vpa(0);
    for j=4:83
        sum=sum+y(j);
    end
    dy(85)=-1*60*dy(85)*sum/4000;
    
   % dy=vpa([5*10^(-4)*y(2)*y(3)-10^(-4)*y(1);-5*10^(-4)*y(2)*y(3)+10^(-4)*y(1);-5*10^(-4)*y(2)*y(3)+10^(-4)*y(1)]);
end